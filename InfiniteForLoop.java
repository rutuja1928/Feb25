public class InfiniteForLoop {
 public static void main(String[] args) {
 for (int i = 0; i < 10; i--) {
 System.out.println(i);
 }
 }
} 

/* 
   initialization: int i = 0;
   condition: i<10 so loop runs as i less than 10
   i-- is decrementing on each iteration
      so is decresing i-- it will getting smaller value like -1, -2 , -3 . The condition i<is always true then result is an infinite loop.

*/


public class InfiniteForLoop {
 public static void main(String[] args) {
 for (int i = 0; i < 10; i++) {
 System.out.println(i);
 }
 }
} 
